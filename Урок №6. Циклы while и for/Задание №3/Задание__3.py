# Запрашиваем у пользователя ввод целых чисел A и B
A = int(input("Введите число A: "))
B = int(input("Введите число B: "))

# Инициализируем пустой список для хранения четных чисел
even_numbers = []

# Проходим по всем числам от A до B включительно
for number in range(A, B + 1):
    if number % 2 == 0:
        even_numbers.append(number)

# Выводим все четные числа через пробел
print("Четные числа на заданном отрезке:", ' '.join(map(str, even_numbers)))
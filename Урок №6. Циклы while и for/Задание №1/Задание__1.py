# Запрашиваем у пользователя ввод числа N
N = int(input("Введите количество чисел N: "))

# Инициализируем счётчик для нулевых чисел
zero_count = 0

# Запрашиваем у пользователя ввод N целых чисел и считаем количество нулей
for i in range(N):
    number = int(input(f"Введите число {i+1}: "))
    if number == 0:
        zero_count += 1

# Выводим результат
print("Количество нулевых чисел:", zero_count)

# Считываем входные данные
m = int(input())  # Максимальная масса лодки
n = int(input())  # Количество рыбаков
weights = [int(input()) for _ in range(n)]  # Веса рыбаков

# Сортируем веса рыбаков
weights.sort()

# Используем два указателя для начала и конца списка
i, j = 0, n - 1
boats = 0

while i <= j:
    # Если самый лёгкий и самый тяжёлый могут плыть вместе
    if weights[i] + weights[j] <= m:
        i += 1  # Переправляем самого лёгкого
    # В любом случае, переправляем самого тяжёлого
    j -= 1
    boats += 1

# Выводим минимальное количество лодок
print(boats)
